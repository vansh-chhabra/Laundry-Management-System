-- D
CREATE TABLE CUSTOMER (
BITSID VARCHAR (13),
CUSTNAME VARCHAR(30),
HOSTEL VARCHAR(10),
ROOMNO INT,
PRIMARY KEY(BITSID),
CHECK (HOSTEL IN ('KR','GD','VK','RM','BD','MR','SH','VY','AK','RP','BH','ML')),
CHECK (ROOMNO>99)
);
-- A
CREATE TABLE BRANCH (
BRANCHID VARCHAR(10),
LOCATION VARCHAR(20) UNIQUE NOT NULL,
PRIMARY KEY (BRANCHID)
);

-- B
CREATE TABLE MONTHLYREVENUE (
BRANCHID VARCHAR(10),
MONTH VARCHAR (3) NOT NULL,
YEAR INT NOT NULL,
INCOME INT,
PRIMARY KEY (BRANCHID,MONTH,YEAR),
FOREIGN KEY(BRANCHID) REFERENCES BRANCH(BRANCHID),
CHECK (INCOME>=0),
CHECK (MONTH IN ('JAN','FEB','MAR','APR','MAY','JUN','JUL','AUG','SEP','OCT','NOV','DEC')),
CHECK (YEAR>2000)
);


-- C
CREATE TABLE ACCOUNT_CUSTOMER(
AccountID VARCHAR(10),
BITSID VARCHAR (13) NOT NULL unique,
OWE INT,
DATEOPENED DATE,
PRIMARY KEY (AccountID),
FOREIGN KEY(BITSID) REFERENCES CUSTOMER(BITSID)
);


-- E
CREATE TABLE CUSTOMER_PHONENO (
BITSID VARCHAR(13),
PHONENO VARCHAR(10),
PRIMARY KEY (BITSID,PHONENO),
FOREIGN KEY(BITSID) REFERENCES CUSTOMER(BITSID)
);
-- F
CREATE TABLE LOGIN_CUSTOMER(
LOGINID VARCHAR (10),
BITSID VARCHAR (13),
PWD VARCHAR (20),
USERNAME VARCHAR (20),
PRIMARY KEY (LOGINID),
FOREIGN KEY(BITSID) REFERENCES CUSTOMER(BITSID)
);
-- G
CREATE TABLE ORDER_RATE_BRANCH_CUSTOMER (
ORDERID VARCHAR (10),
BITSID VARCHAR (13),
TYPEID VARCHAR (10),
BRANCHID VARCHAR (10),
