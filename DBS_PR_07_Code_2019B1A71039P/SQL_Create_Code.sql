PRIMARY KEY (ORDERID),
FOREIGN KEY(BITSID) REFERENCES CUSTOMER(BITSID),
FOREIGN KEY(TYPEID) REFERENCES RATE(TYPEID),
FOREIGN KEY(BRANCHID) REFERENCES BRANCH(BRANCHID)

);
